<script>
export default {
  data() {
    return {
      mainLink:
        "/redirect/credible-mortgage-loan-review/main",
    };
  },
  mounted() {
    this.$nextTick(() => {
      function init() {
        function screenScroll() {
          let targetDom = $(".fixed-container");

          let left = $(".credible-page-container").offset().left;

          let offsetTop = targetDom.offset().top;
          let screenHeight = $(window).height();
          let headerHeight = $(".header-container").height();
          let footerTop = $(".footer-container").offset().top;
          let targetHeight = targetDom.height();
          let pageHeight = $(".credible-page-container").height();
          $(window).on("scroll", function () {
            let scrollTop = $(this).scrollTop();
            if (scrollTop + headerHeight > offsetTop) {
              if (scrollTop + headerHeight + targetHeight > footerTop) {
                targetDom.css({
                  position: "absolute",
                  top: pageHeight - targetHeight,
                  right: 0,
                });
              } else {
                targetDom.css({
                  position: "fixed",
                  top: headerHeight,
                  right: left,
                });
              }
            } else {
              targetDom.css({
                position: "static",
              });
            }

            // 滚动的时候循环遍历左侧内容区
            $(".quick-title").each(function (i) {
              let top = $(this).offset().top;

              if (scrollTop + screenHeight - screenHeight / 5 > top) {
                $(".quick-list li").each(function (index) {
                  if (index != i) {
                    $(this).removeClass("current");
                  } else {
                    $(this).addClass("current");
                  }
                });
              } else {
                $(".quick-list li").eq(i).removeClass("current");
              }
            });
          });
        }

        screenScroll();

        let screenHeight = $(window).height();

        $(".quick-list li").on("click", function () {
          let index = $(".quick-list li").index($(this));

          let top = $(".quick-title").eq(index).offset().top;

          window.scrollTo({
            top: top - screenHeight / 5,
            behavior: "smooth",
          });
        });
      }
      init();
    });
  },
};
</script>

