                        <h2>It’s Simple to Start Your Personal Loan Search</h2>
                        <p>If you’re on the hunt for a personal loan, you may be wondering, what <i>is</i> a personal loan, exactly? <b>A personal loan is money you borrow from a lending institution — be it a bank, credit union, or other third-party lender—which you will pay back over a fixed period of time.</b> In today’s digital age, there are plenty of reputable online loans to choose from, which can be great when comparing personal loan rates.</p>
                        <p>There are several steps involved that you’ll want to consider when searching for personal loans online. Let’s go through each of them:</p>
                        <h3>Step 1: Where to get a personal loan</h3>
                        <p>First thing’s first. Where do you get a personal loan? As mentioned earlier in this article, you can obtain a personal loan from a bank, credit union, or online lending partner. A bank is the most traditional route, but credit unions are known for offering more flexible terms and rates.</p>
                        <p>You can also find personal loans online. It may require a bit more research to ensure they’re reputable and safe to use, but they can provide a convenient means to obtaining an online personal loan quickly.</p>
                        <h3>Step 2: Get pre-qualified</h3>
                        <p>If you want to get a quick idea of what kind of loans you qualify for — be it low interest personal loans, small personal loans, quick personal loans, and so on — get pre-qualified.</p>
                        <p>Many online lending partners can get you pre-qualification rates by conducting a <b>soft credit check</b>, which will not harm your credit score. Some information you may be asked for during this process includes the following:</p>
