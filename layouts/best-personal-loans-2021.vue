        let clickIndex = $(".column").index(this);
        $(".menu-column").css({ display: "none" });
        $(".menu-column").eq(clickIndex).css({ display: "flex" });
      });
    },
  },
  destroyed() {
    clearTimeout(this.timer);
  },
